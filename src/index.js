const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let i = 0;
    let ans = '';
    while (i<expr.length){
        let letter = '';
        for(let j = i; j<i+9; j++){
            if(expr[j]==='1'){
                if(expr[j+1]==="0"){
                    letter+='.';
                }else if(expr[j+1]==="1"){
                    letter+='-';
                    j++;
                }
            }else if(expr[j]==='*'){
                ans+=' ';
                break;
            }
        }
        if(letter!=''){
            ans+=MORSE_TABLE[letter];
        }
        
        i+=10
    }
    return ans;
}

